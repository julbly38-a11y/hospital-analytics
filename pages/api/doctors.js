export const MONTHS = {
  'січн':1,'лютог':2,'лютн':2,'берез':3,'квітн':4,'травн':5,
  'червн':6,'липн':7,'серпн':8,'верес':9,'жовтн':10,'листопад':11,'груд':12
}

export function detectDoctor(t) {
  for (const s of DOCTOR_SURNAMES) { if (t.includes(s)) return s }
  return null
}

export function detectYear(t) {
  const m = t.match(/20(1[5-9]|2[0-5])/)
  return m ? m[0] : null
}

export function detectMonth(t) {
  for (const [k,v] of Object.entries(MONTHS)) { if (t.includes(k)) return v }
  return null
}

export function detectPeriod(t, year) {
  const y = year || 2024
  if (t.includes('перш') && t.includes('півріч')) return {from:`${y}-01-01`,to:`${y}-06-30`}
  if (t.includes('друг') && t.includes('півріч')) return {from:`${y}-07-01`,to:`${y}-12-31`}
  if (t.includes('q1') || (t.includes('1') && t.includes('кварт'))) return {from:`${y}-01-01`,to:`${y}-03-31`}
  if (t.includes('q2') || (t.includes('2') && t.includes('кварт'))) return {from:`${y}-04-01`,to:`${y}-06-30`}
  if (t.includes('q3') || (t.includes('3') && t.includes('кварт'))) return {from:`${y}-07-01`,to:`${y}-09-30`}
  if (t.includes('q4') || (t.includes('4') && t.includes('кварт'))) return {from:`${y}-10-01`,to:`${y}-12-31`}
  const month = detectMonth(t)
  if (month) {
    const m = String(month).padStart(2,'0')
    const lastDay = new Date(y, month, 0).getDate()
    return {from:`${y}-${m}-01`,to:`${y}-${m}-${lastDay}`}
  }
  if (year) return {from:`${year}-01-01`,to:`${year}-12-31`}
  return null
}
