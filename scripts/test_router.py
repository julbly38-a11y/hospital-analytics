def test_query(question):
    payload = json.dumps({"question": question, "provider": "groq"}).encode()
    req = urllib.request.Request(
        f"{BASE_URL}/api/ask",
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST"
    )
    try:
        with urllib.request.urlopen(req, timeout=15) as resp:
            data = json.loads(resp.read())
            if data.get("error"):
                return "ERROR", data["error"], None, None
            rows = len(data.get("rows", []))
            provider = data.get("tokens", {}).get("provider", "?")
            return "OK", None, rows, provider
    except Exception as e:
        return "EXCEPTION", str(e), None, None

def main():
    ok = []
    errors = []
    empty = []

    print(f"{'='*70}")
    print(f"Тестування роутера: {len(EXAMPLES)} запитів")
    print(f"{'='*70}\n")

    for i, q in enumerate(EXAMPLES, 1):
        status, err, rows, provider = test_query(q)
        prefix = f"[{i:02d}/{len(EXAMPLES)}]"

        if status == "OK" and rows == 0:
            empty.append(q)
            print(f"{prefix} ⚠️  EMPTY   {provider:20s} | {q}")
        elif status == "OK":
            ok.append(q)
            print(f"{prefix} ✅ OK({rows:3d}) {provider:20s} | {q}")
        else:
            errors.append((q, err))
            print(f"{prefix} ❌ ERROR   {'':20s} | {q}")
            print(f"         → {err[:80]}")

        time.sleep(0.3)  # щоб не перевантажити Groq

    print(f"\n{'='*70}")
    print(f"РЕЗУЛЬТАТ: ✅ {len(ok)} OK  ⚠️  {len(empty)} порожніх  ❌ {len(errors)} помилок")
    print(f"{'='*70}")

    if empty:
        print(f"\n⚠️  ПОРОЖНІ РЕЗУЛЬТАТИ ({len(empty)}):")
        for q in empty:
            print(f"  - {q}")

    if errors:
        print(f"\n❌ ПОМИЛКИ ({len(errors)}):")
        for q, err in errors:
            print(f"  - {q}")
            print(f"    {err[:100]}")

if __name__ == "__main__":
    main()
