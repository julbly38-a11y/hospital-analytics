/**
 * Повертає іконку для відділення. Якщо не знайдено — '⊡'.
 * @param {string} deptName
 * @returns {string}
 */
export function deptIcon(deptName) {
  if (!deptName) return '⊡'
  // Точний збіг
  if (DEPT_ICONS[deptName]) return DEPT_ICONS[deptName]
  // Частковий збіг за ключовими словами
  const d = deptName.toLowerCase()
  if (d.includes('хірург'))      return '✂'
  if (d.includes('нейрохірург')) return '⚡'
  if (d.includes('травматол'))   return '⊕'
  if (d.includes('терапевт'))    return '♡'
  if (d.includes('кардіол'))     return '♥'
  if (d.includes('невролог') || d.includes('неврол')) return '⌥'
  if (d.includes('гастроентерол')) return '≈'
  if (d.includes('гематол'))     return '◈'
  if (d.includes('урол'))        return '◉'
  if (d.includes('опіков'))      return '⌘'
  if (d.includes('анестез') || d.includes('інтенсив') || d.includes('реанімац')) return '◎'
  if (d.includes('гінекол'))     return '♀'
  if (d.includes('ендокрин'))    return '⊙'
  if (d.includes('онкол'))       return '⊗'
  if (d.includes('інфекц'))      return '⚬'
  if (d.includes('пульмонол'))   return '≋'
  if (d.includes('нефрол'))      return '◎'
  if (d.includes('приймал'))     return '↘'
  if (d.includes('лабор'))       return '℞'
  return '⊡'
}

